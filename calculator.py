#calculator
numr = float(input('enter first number : \n'))
num = float(input('enter second number:\n'))
op = input('choose operation : + , - , /, *, // \n')

if op == '+':
	print(numr+num)
elif op == '-':
	print(numr - num)
elif op == '*':
	print(numr * num)
elif op == '//':
	print(numr//num)
else :
	print(numr/num)


	




